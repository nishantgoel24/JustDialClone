package com.beans;

public class BlogAdsRegisterBean {

	private int registrationid;

	private String firstname;
	private String lastname;
	private String emailid;
	private String mobile;
	private String address;
	private String locationactual;
	private String locationnear;
	private String password;

	public int getRegistrationid() {
		return registrationid;
	}

	public void setRegistrationid(int registrationid) {
		this.registrationid = registrationid;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getEmailid() {
		return emailid;
	}

	public void setEmailid(String emailid) {
		this.emailid = emailid;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getLocationactual() {
		return locationactual;
	}

	public void setLocationactual(String locationactual) {
		this.locationactual = locationactual;
	}

	public String getLocationnear() {
		return locationnear;
	}

	public void setLocationnear(String locationnear) {
		this.locationnear = locationnear;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}
